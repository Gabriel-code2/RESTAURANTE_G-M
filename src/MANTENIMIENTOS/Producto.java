    private void idProductoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_idProductoActionPerformed

        if (evt.getSource() == idProducto) {

            int cod;
            boolean encontrado = false;

            cod = Integer.parseInt(idProducto.getText());

            Scanner s;

            try {
                File f = new File("Productos.txt");
                s = new Scanner(f);
                if (!f.exists()) {

                    f.createNewFile();

                } else {
                    while (s.hasNextLine() && !encontrado) {
                        String linea = s.nextLine();
                        Scanner sl = new Scanner(linea);
                        sl.useDelimiter("\\s*;\\s*");
                        try {
                            if (cod == Integer.parseInt(sl.next())) {
                                descripciom.setText(sl.next());
                                existencia.setText(sl.next());
                                tipo.setText(sl.next());
                                precio.setText(sl.next());
                                existencia.setText("0");
                                String auxitbis = "";
                                if (itb.equals(sl.next())) {
                                    Si.setSelected(true);
                                    auxitbis = "Si";
                                } else {
                                    No.setSelected(true);
                                    auxitbis = "NO";
                                }

                                encontrado = true;
                                creear = true;

                                Santigualinea = idProducto.getText() + "; " + descripciom.getText() + "; " + tipo.getText() + "; " + precio.getText() + "; " + existencia.getText() + "; " + auxitbis;

                                estado.setText("MODIFICANDO");

                            } else {

                                descripciom.setText("");
                                existencia.setText("");
                                tipo.setText("");
                                precio.setText("");

                                encontrado = false;
                                creear = false;
                                estado.setText("CREANDO");
                            }
                        } catch (Exception el) {
                            el.printStackTrace();
                        }
                    }
                }
                s.close();
            } catch (FileNotFoundException el) {
                JOptionPane.showMessageDialog(null, "Error al leer Arcivo" + el);
            } catch (IOException el) {
                el.printStackTrace();
            }

        }
    }//GEN-LAST:event_idProductoActionPerformed

    private void tipoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tipoActionPerformed
        if (evt.getSource() == tipo) {
            String auxid = tipo.getText();
            String nombreTipo = "";

            boolean encontrado = false;
            try {
                int Id_Tipo = Integer.parseInt(auxid);
                try {
                    File f = new File("TipoProducto.txt");
                    Scanner s = new Scanner(f);
                    if (f.exists()) {

                        while (s.hasNextLine() && !encontrado) {
                            String linea = s.nextLine();
                            Scanner s1 = new Scanner(linea);
                            s1.useDelimiter("\\s*;\\s*");

                            if (Id_Tipo == Integer.parseInt(s1.next())) {
                                encontrado = true;
                                nombreTipo = s1.next();
                            }

                        }
                        s.close();

                        if (!encontrado) {
                            JOptionPane.showMessageDialog(this, "El Producto no Existe", "Error", JOptionPane.ERROR_MESSAGE);

                        } else {
                            JOptionPane.showMessageDialog(rootPane, "El Nombre del Producto es: " + nombreTipo);
                        }

                    } else {
                        JOptionPane.showMessageDialog(this, "El Producto no Existe", "Error", JOptionPane.ERROR_MESSAGE);

                    }
                } catch (IOException e) {
                    System.out.println("No se pudo leer el archivo correctamente\n" + e);
                }

            } catch (Exception ex) {
                tipo.setText("");
            }
        }


    }//GEN-LAST:event_tipoActionPerformed

