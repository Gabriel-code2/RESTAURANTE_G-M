    private void btnGuardar2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardar2ActionPerformed
        if (idProducto.getText().equals("") || descripciom.getText().equals("") || tipo.getText().equals("")
                || precio.getText().equals("") || !Si.isSelected() && !No.isSelected()) {

            JOptionPane.showMessageDialog(null, "Por Favor Rellene Todos los campos ");
        } else {

            String auxDisponible = "";

            if (Si.isSelected()) {
                auxDisponible = "Si";
            } else {
                auxDisponible = " NO ";
            }

            String Id = "";
            String des = "", exi = "", tip = "", pre = "";
            String Snuevalinea = "";

            ArchivoProductos MA = new ArchivoProductos();

            Id = idProducto.getText();
            des = descripciom.getText();
            exi = existencia.getText();
            tip = tipo.getText();
            pre = precio.getText();

            try {

                if (creear == false) {
                    MA.GuardarDatos(Id, des, exi, tip, pre, auxDisponible);
                } else {
                    Snuevalinea = (Id + "; " + des + "; " + exi + "; " + tip + "; " + pre + "; " + auxDisponible);
                    MA.Modificar(Santigualinea, Snuevalinea);
                }
                idProducto.setText("");
                descripciom.setText("");
                existencia.setText("");
                tipo.setText("");
                botones.clearSelection();

                precio.setText("");
                estado.setText("");

            } catch (IOException el) {

                el.printStackTrace();
            }
            JOptionPane.showMessageDialog(null, "Registro guardado");

        }
    }//GEN-LAST:event_btnGuardar2ActionPerformed

