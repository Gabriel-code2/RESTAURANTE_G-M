    private void IdProductoKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_IdProductoKeyReleased
    if (evt.getKeyCode()==KeyEvent.VK_ENTER) {
            int cod;

                cod = Integer.parseInt(IdProducto.getText());
                    boolean encontrado = false;

                Scanner s;

                try {
                    File f = new File("TipoProducto.txt");
                    s = new Scanner(f);
                    if (!f.exists()) {

                        f.createNewFile();

                    } else {
                        while (s.hasNextLine() && !encontrado) {
                            String linea = s.nextLine();
                            Scanner sl = new Scanner(linea);
                            sl.useDelimiter("\\s*;\\s*");
                            try {
                                if (cod == Integer.parseInt(sl.next())) {
                                    Nombre.setText(sl.next());
                                    encontrado = true;
                                    creear = true;

                                    Santigualinea = IdProducto.getText() + " ; " + Nombre.getText();

                                    estado.setText("MODIFICANDO");

                                } else {
                                     Nombre.setText("");
                                     
                                    encontrado = false;
                                    creear = false;
                                    estado.setText("CREANDO");
                                }
                            } catch (Exception el) {
                                el.printStackTrace();
                            }
                        }
                    }
                    s.close();
                } catch (FileNotFoundException el) {
                    JOptionPane.showMessageDialog(null, "Error al leer Arcivo" + el);
                } catch (IOException el) {
                    el.printStackTrace();
                }
         }        // TODO add your handling code here:
    }//GEN-LAST:event_IdProductoKeyReleased

