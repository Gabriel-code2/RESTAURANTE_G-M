    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        if(IdProducto.getText().equals("") || Nombre.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Por Favor Rellene Todos los campos ");
        }else{
            
        String Id = "";
        String nombre = "";
        String Snuevalinea = "";

        Archivo_TipoProducto archivo = new Archivo_TipoProducto();
        Id = IdProducto.getText();
        nombre = Nombre.getText();

        try {

            if (creear == false) {
                archivo.GuardarDatos(Id, nombre);
            } else {
                Snuevalinea = (Id + "; " + nombre);
                archivo.Modificar(Santigualinea, Snuevalinea);
            }

            IdProducto.setText("");
            Nombre.setText("");
            estado.setText("");

        } catch (IOException el) {

            el.printStackTrace();
        }
          JOptionPane.showMessageDialog(null, "Registro guardado");
         
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

